#include "./libft.h"

void		ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*cpy;

	i = 0;
	cpy = (unsigned char*)s;
	while (i < n)
	{
		cpy[i] = 0;
		i = i + 1;
	}
}
